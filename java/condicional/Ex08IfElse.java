import java.util.*;

public class Ex08IfElse {
    public static void main(String[] args) {
      int nota = 2;
      
      if (nota >= 5) {
        System.out.println("Aprovado");
      }
      
      else {
        System.out.print("Reprovado");
      }
    }
}
